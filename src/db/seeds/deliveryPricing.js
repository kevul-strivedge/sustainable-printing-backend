async function seed() {
  console.log('Seeding delivery pricing tables...');

  await db.delete(ptDeliveryPrices);
  await db.delete(ptDeliveryPostcodesRanges);
  await db.delete(ptDeliveryWeights);
  await db.delete(ptDeliveryZones);

  await db.insert(ptDeliveryZones).values(zones);
  console.log(`  ✓ ${zones.length} zones`);

  await db.insert(ptDeliveryWeights).values(weights);
  console.log(`  ✓ ${weights.length} weight brackets`);

  await db.insert(ptDeliveryPostcodesRanges).values(postcodeRanges);
  console.log(`  ✓ ${postcodeRanges.length} postcode ranges`);

  // Insert prices in chunks to avoid query size limits
  const chunkSize = 50;
  for (let i = 0; i < prices.length; i += chunkSize) {
    await db.insert(ptDeliveryPrices).values(prices.slice(i, i + chunkSize));
  }
  console.log(`  ✓ ${prices.length} delivery prices`);

  console.log('Done.');
  process.exit(0);
}

seed().catch((err) => { console.error(err); process.exit(1); });
